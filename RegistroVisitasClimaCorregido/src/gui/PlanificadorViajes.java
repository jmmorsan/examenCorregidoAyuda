    public void actualizarImagenEstacion(String estacion) {
    switch (estacion) {
        case "Primavera":
            jLabelImagen.setIcon(escalarImagen("/img/primavera.jpg", 200, 150));
            break;

        case "Verano":
            jLabelImagen.setIcon(escalarImagen("/img/verano.jpg", 200, 150));
            break;

        case "Otoño":
            jLabelImagen.setIcon(escalarImagen("/img/otono.jpg", 200, 150));
            break;

        case "Invierno":
            jLabelImagen.setIcon(escalarImagen("/img/invierno.jpg", 200, 150));
            break;

        default:
            jLabelImagen.setIcon(null);
    }
    }
    
    private ImageIcon escalarImagen(String ruta, int ancho, int alto) {
    ImageIcon icon = new ImageIcon(getClass().getResource(ruta));
    Image img = icon.getImage();
    Image imgEscalada = img.getScaledInstance(ancho, alto, Image.SCALE_SMOOTH);
    return new ImageIcon(imgEscalada);
}


