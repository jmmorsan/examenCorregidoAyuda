    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
    while(true){    
        Date estacion;
        String ciudadStr = JOptionPane.showInputDialog(this,
        "Introduce la ciudad de la visita que deseas eliminar:",
        "Eliminación de visita",JOptionPane.QUESTION_MESSAGE);
        // Si el usuario cancela el diálogo → salir del método
        
        if (ciudadStr == null) {
            return;
        }
            try {
                int respuesta = JOptionPane.showConfirmDialog(this,
                "¿Estás seguro de eliminar la visita programada hacia " + ciudadStr + "?",
                "Confirmar eliminación",JOptionPane.YES_NO_OPTION);
                
        if (respuesta == JOptionPane.YES_OPTION) {
            boolean eliminado = LogicaRegistro.eliminarRegistroPorCiudad(ciudadStr);
            if (eliminado) {
            JOptionPane.showMessageDialog(this,
            "De acuerdo, eliminamos la visita a la ciudad " + ciudadStr + " en la estacion ",
            "Fin del proceso",JOptionPane.INFORMATION_MESSAGE);
            
            refrescarTabla(); // Actualiza la tabla
            break; // Salir del bucle tras eliminar
        } else {
                JOptionPane.showMessageDialog(this,
                "No se encontró ninguna visita con destino a: " + ciudadStr,
                "No encontrado", JOptionPane.ERROR_MESSAGE);
        // Repetir el bucle para dar otra oportunidad
        }
        } else {
                JOptionPane.showMessageDialog(this,"Sabia que no te querías perder la visita a la ciudad " + ciudadStr + "en la fecha ",
                "Cancelado",JOptionPane.INFORMATION_MESSAGE);
                break; // Salir si el usuario cancela
        }
        }catch(NumberFormatException e){
            
        }
    }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

