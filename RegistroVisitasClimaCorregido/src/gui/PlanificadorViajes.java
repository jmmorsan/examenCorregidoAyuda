    public void refrescarTabla(){
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yy");
        
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.setColumnIdentifiers(new String[]{"Ciudad a visitar", "Fecha de visita prevista",
                                              "Estacion del año","Temperatura mínima prevista en ºC",
                                              "Temperatura máxima prevista en ºC", "Temperatura promedio"});
        
        List<Registro> listaRegistros = LogicaRegistro.getRegistro();
            for (Registro registro: listaRegistros){
                dtm.addRow(new Object[]{
                registro.getCiudad(),
                sdf.format(registro.getFechaVisita()),
                registro.getEstacion(),
                registro.getTempMin(),
                registro.getTempMax(),
                registro.getTempProm()});
        }
        // **PASO 1:** Establece el nuevo modelo (con datos)
        jTableRegistros.setModel(dtm);
        
        // **PASO 2:** Aplicar el sorter al NUEVO modelo que acabas de establecer
        TableRowSorter<TableModel> sorter = new TableRowSorter<>(jTableRegistros.getModel());
        jTableRegistros.setRowSorter(sorter);
        
        // **PASO 3:** Definir que la columna 5 (Temperatura promedio) es numérica
        sorter.setComparator(5, (o1, o2) -> {
        Double d1 = Double.parseDouble(o1.toString());
        Double d2 = Double.parseDouble(o2.toString());
        return d1.compareTo(d2);
        });
        
        // **PASO 4:** Ordenar automáticamente de MAYOR a MENOR
        sorter.setSortKeys(
        java.util.Arrays.asList(
        new javax.swing.RowSorter.SortKey(5, javax.swing.SortOrder.DESCENDING)
            )
        );
        
        // El repaint no es estrictamente necesario, pero no estorba.
        jTableRegistros.repaint();
    }

